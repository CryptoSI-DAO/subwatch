/**
 * Subscription template library for SubWatch.
 *
 * A curated catalog of popular subscription services that users can quickly
 * add to their tracked subscriptions. Prices reflect typical 2024-2025
 * consumer pricing and are intended as sensible defaults — users can override
 * them when adding a subscription.
 */

export type Category =
  | 'Entertainment'
  | 'Music'
  | 'Cloud Storage'
  | 'News'
  | 'Fitness'
  | 'Productivity'
  | 'Shopping'
  | 'Communication'
  | 'Security'
  | 'Other';

export type Currency = 'USD';

export type BillingCycle = 'monthly' | 'yearly';

export interface Template {
  /** Unique, URL-friendly identifier (kebab-case). */
  id: string;
  /** Human-readable display name. */
  name: string;
  /** High-level category used for grouping and filtering. */
  category: Category;
  /** Default price in whole units (e.g. dollars). */
  defaultPrice: number;
  /** ISO 4217 currency code. */
  currency: Currency;
  /** Whether the default price is billed monthly or yearly. */
  billingCycle: BillingCycle;
  /** Emoji used as a lightweight icon in the UI. */
  icon: string;
  /** Brand-ish accent color as a hex string. */
  color: string;
}

